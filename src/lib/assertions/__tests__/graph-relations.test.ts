import { beforeEach, describe, expect, it, vi } from "vitest";

const { prismaMock } = vi.hoisted(() => ({
  prismaMock: {
    $transaction: vi.fn(),
    paper: {
      findFirst: vi.fn(),
      update: vi.fn(),
    },
    paperRelation: {
      findMany: vi.fn(),
      findUnique: vi.fn(),
      deleteMany: vi.fn(),
    },
    relationAssertion: {
      deleteMany: vi.fn(),
    },
    paperEntity: {},
    paperIdentifier: {},
    paperEntityCandidateLink: {},
  },
}));

vi.mock("../../prisma", () => ({
  prisma: prismaMock,
}));

vi.mock("../../canonical/entity-service", () => ({
  collectIdentifiers: vi.fn(),
  resolveOrCreateEntity: vi.fn(),
}));

vi.mock("../relation-assertion-service", () => ({
  createRelationAssertion: vi.fn(),
}));

vi.mock("../relation-aggregate", () => ({
  getAggregatedRelationsForPaper: vi.fn(),
}));

vi.mock("../legacy-projection", () => ({
  projectLegacyRelation: vi.fn(),
}));

import {
  GraphRelationError,
  buildLegacyOverlayRows,
  clearAutoGeneratedRelationsForPaper,
  createManualRelation,
  ensurePaperEntityForGraph,
  listRelationsForPaper,
} from "../graph-relations";
