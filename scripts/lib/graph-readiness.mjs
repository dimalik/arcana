function makeRowSignature(row) {
  return [
    row.relatedPaper.id,
    row.relationType,
    row.description ?? "",
    Number(row.confidence ?? 0).toFixed(6),
    row.isAutoGenerated ? "auto" : "manual",
  ].join("::");
}

function sortRows(rows) {
  return [...rows].sort((a, b) => {
    const keyA = makeRowSignature(a);
    const keyB = makeRowSignature(b);
    return keyA.localeCompare(keyB);
  });
}

function rowsEqual(a, b) {
  if (a.length !== b.length) return false;
  const left = sortRows(a);
  const right = sortRows(b);
  return left.every((row, index) => makeRowSignature(row) === makeRowSignature(right[index]));
}

function buildPeerMap(rows) {
  const peers = new Map();

  for (const row of rows) {
    const peerId = row.relatedPaper.id;
    const existing = peers.get(peerId) ?? {
      peerPaperId: peerId,
      rowCount: 0,
      relationTypes: new Set(),
      signatures: new Set(),
    };
    existing.rowCount += 1;
    existing.relationTypes.add(row.relationType);
    existing.signatures.add(makeRowSignature(row));
    peers.set(peerId, existing);
  }

  return peers;
}

function mapToComparableObject(map) {
  const result = {};
  for (const [peerId, value] of map.entries()) {
    result[peerId] = {
      rowCount: value.rowCount,
      relationTypes: [...value.relationTypes].sort(),
      signatures: [...value.signatures].sort(),
    };
  }
  return result;
}

export function buildMultiplicitySummary(beforeRows, afterRows) {
  const before = buildPeerMap(beforeRows);
  const after = buildPeerMap(afterRows);
  const fanOutPeers = [];

  for (const [peerId, afterPeer] of after.entries()) {
    const beforePeer = before.get(peerId);
    if (!beforePeer) continue;
    if (beforePeer.rowCount === 1 && afterPeer.rowCount > 1) {
      fanOutPeers.push({
        peerPaperId: peerId,
        relationTypes: [...afterPeer.relationTypes].sort(),
      });
    }
  }

  return {
    peerCountBefore: before.size,
    peerCountAfter: after.size,
    rowCountBefore: beforeRows.length,
    rowCountAfter: afterRows.length,
    fanOutPeers: fanOutPeers.sort((a, b) => a.peerPaperId.localeCompare(b.peerPaperId)),
    beforePeers: mapToComparableObject(before),
    afterPeers: mapToComparableObject(after),
  };
}

export function classifyParityChange({
  beforeMode,
  beforeRows,
  afterMode,
  afterRows,
}) {
  const multiplicity = buildMultiplicitySummary(beforeRows, afterRows);
  const beforePeers = new Set(Object.keys(multiplicity.beforePeers));
  const afterPeers = new Set(Object.keys(multiplicity.afterPeers));

  const missingPeers = [...beforePeers]
    .filter((peerId) => !afterPeers.has(peerId))
    .sort();
  const addedPeers = [...afterPeers]
    .filter((peerId) => !beforePeers.has(peerId))
    .sort();

  const changedPeers = [];
  for (const peerId of [...beforePeers].filter((peer) => afterPeers.has(peer)).sort()) {
    const beforePeer = multiplicity.beforePeers[peerId];
    const afterPeer = multiplicity.afterPeers[peerId];
    if (
      beforePeer.rowCount !== afterPeer.rowCount ||
      JSON.stringify(beforePeer.relationTypes) !== JSON.stringify(afterPeer.relationTypes) ||
      JSON.stringify(beforePeer.signatures) !== JSON.stringify(afterPeer.signatures)
    ) {
      changedPeers.push({
        peerPaperId: peerId,
        before: beforePeer,
        after: afterPeer,
      });
    }
  }

  if (beforeMode === afterMode && rowsEqual(beforeRows, afterRows)) {
    return {
      classification: "exact_parity",
      multiplicity,
      mismatchDetails: null,
    };
  }

  const hasOnlyFanOut =
    missingPeers.length === 0 &&
    addedPeers.length === 0 &&
    changedPeers.length === multiplicity.fanOutPeers.length &&
    changedPeers.length > 0;

  if ((beforeMode !== afterMode || multiplicity.fanOutPeers.length > 0) && hasOnlyFanOut) {
    return {
      classification: "expected_mode_switch",
      multiplicity,
      mismatchDetails: {
        beforeMode,
        afterMode,
        reason: "multiplicity_fan_out",
        fanOutPeers: multiplicity.fanOutPeers,
      },
    };
  }

  if (beforeMode !== afterMode && rowsEqual(beforeRows, afterRows)) {
    return {
      classification: "expected_mode_switch",
      multiplicity,
      mismatchDetails: {
        beforeMode,
        afterMode,
        reason: "mode_switch_same_rows",
        fanOutPeers: multiplicity.fanOutPeers,
      },
    };
  }

  return {
    classification: "true_mismatch",
    multiplicity,
    mismatchDetails: {
      beforeMode,
      afterMode,
      missingPeers,
      addedPeers,
      changedPeers,
      fanOutPeers: multiplicity.fanOutPeers,
    },
  };
}

export function validateWaivers(waivers, writeVisibilityByPaperId) {
  const errors = [];
  const normalizedWaivers = [];

  for (const waiver of waivers) {
    const visibility = writeVisibilityByPaperId.get(waiver.paperId) ?? {
      owner_userId: null,
      rendered_in_relations_ui: false,
      checked_at: new Date().toISOString(),
    };

    const normalized = {
      ...waiver,
      write_visible: Boolean(waiver.write_visible),
      write_visibility_check: waiver.write_visibility_check ?? visibility,
    };

    if (normalized.write_visible) {
      errors.push(`Waiver ${waiver.paperId} is invalid: write_visible must be false`);
    }

    if (normalized.write_visibility_check.rendered_in_relations_ui) {
      errors.push(
        `Waiver ${waiver.paperId} is invalid: paper is write-visible in relations UI`
      );
    }

    normalizedWaivers.push(normalized);
  }

  return {
    normalizedWaivers,
    errors,
  };
}

export function emptyWaiversFile() {
  return { waivers: [] };
}
