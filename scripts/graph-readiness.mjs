#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import Database from "better-sqlite3";

import {
  classifyParityChange,
  emptyWaiversFile,
  validateWaivers,
} from "./lib/graph-readiness.mjs";

function parseArgs(argv) {
  const args = {
    dbPath: path.join(process.cwd(), "prisma", "dev.db"),
    entityOut: null,
    parityOut: null,
    waiversPath: path.join(process.cwd(), "benchmark", "graph", "waivers.json"),
    baselinePath: null,
  };

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--db-path") args.dbPath = argv[++i];
    else if (arg === "--entity-out") args.entityOut = argv[++i];
    else if (arg === "--parity-out") args.parityOut = argv[++i];
    else if (arg === "--waivers") args.waiversPath = argv[++i];
    else if (arg === "--baseline") args.baselinePath = argv[++i];
    else if (arg === "--help") {
      console.log(
        "Usage: node scripts/graph-readiness.mjs " +
          "[--db-path <path>] [--entity-out <path>] [--parity-out <path>] " +
          "[--waivers <path>] [--baseline <pre-cutover-parity.json>]"
      );
      process.exit(0);
    }
  }

  return args;
}

function ensureDirFor(filePath) {
  if (!filePath) return;
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function readJson(filePath, fallback) {
  if (!filePath || !fs.existsSync(filePath)) return fallback;
  return JSON.parse(fs.readFileSync(filePath, "utf-8"));
}

function writeJson(filePath, value) {
  ensureDirFor(filePath);
  fs.writeFileSync(filePath, JSON.stringify(value, null, 2) + "\n");
}

function normalizeRelationRow(row) {
  return {
    id: row.id,
    relatedPaper: {
      id: row.relatedPaper.id,
    },
    relationType: row.relationType,
    description: row.description ?? null,
    confidence: Number(row.confidence ?? 0),
    isAutoGenerated: Boolean(row.isAutoGenerated),
  };
}

function makePrepared(db) {
  const paperColumns = new Set(
    db.prepare(`PRAGMA table_info("Paper")`).all().map((column) => column.name)
  );
  const semanticScholarSelect = paperColumns.has("semanticScholarId")
    ? "p.semanticScholarId AS semanticScholarId"
    : "NULL AS semanticScholarId";

  return {
    denominatorPapers: db.prepare(`
      SELECT DISTINCT
        p.id,
        p.userId,
        p.entityId,
        p.title,
        p.authors,
        p.year,
        p.venue,
        p.abstract,
        p.doi,
        p.arxivId,
        ${semanticScholarSelect}
      FROM Paper p
      WHERE p.userId IS NOT NULL
        AND (
          EXISTS (
            SELECT 1
            FROM PaperRelation pr
            WHERE pr.sourcePaperId = p.id OR pr.targetPaperId = p.id
          )
          OR EXISTS (
            SELECT 1
            FROM RelationAssertion ra
            WHERE ra.sourcePaperId = p.id
          )
          OR (
            p.entityId IS NOT NULL AND EXISTS (
              SELECT 1
              FROM RelationAssertion ra
              JOIN Paper sp ON sp.id = ra.sourcePaperId
              WHERE sp.userId = p.userId
                AND (ra.sourceEntityId = p.entityId OR ra.targetEntityId = p.entityId)
            )
          )
        )
      ORDER BY p.id
    `),
    sourceRelations: db.prepare(`
      SELECT
        pr.id,
        pr.relationType,
        pr.description,
        pr.confidence,
        pr.isAutoGenerated,
        tp.id AS relatedPaperId,
        tp.title AS relatedPaperTitle,
        tp.year AS relatedPaperYear,
        tp.authors AS relatedPaperAuthors
      FROM PaperRelation pr
      JOIN Paper tp ON tp.id = pr.targetPaperId
      WHERE pr.sourcePaperId = ?
      ORDER BY pr.confidence DESC, pr.id ASC
    `),
    targetRelations: db.prepare(`
      SELECT
        pr.id,
        pr.relationType,
        pr.description,
        pr.confidence,
        pr.isAutoGenerated,
        sp.id AS relatedPaperId,
        sp.title AS relatedPaperTitle,
        sp.year AS relatedPaperYear,
        sp.authors AS relatedPaperAuthors
      FROM PaperRelation pr
      JOIN Paper sp ON sp.id = pr.sourcePaperId
      WHERE pr.targetPaperId = ?
      ORDER BY pr.confidence DESC, pr.id ASC
    `),
    aggregateAssertions: db.prepare(`
      SELECT
        ra.sourceEntityId,
        ra.targetEntityId,
        ra.relationType,
        ra.confidence,
        ra.provenance,
        ra.description,
        te.id AS targetEntityIdResolved,
        te.title AS targetEntityTitle,
        te.year AS targetEntityYear,
        te.authors AS targetEntityAuthors,
        se.id AS sourceEntityIdResolved,
        se.title AS sourceEntityTitle,
        se.year AS sourceEntityYear,
        se.authors AS sourceEntityAuthors
      FROM RelationAssertion ra
      LEFT JOIN PaperEntity te ON te.id = ra.targetEntityId
      LEFT JOIN PaperEntity se ON se.id = ra.sourceEntityId
      WHERE ra.sourcePaperId = ?
         OR (
           ra.targetEntityId = ?
           AND EXISTS (
             SELECT 1 FROM Paper sp
             WHERE sp.id = ra.sourcePaperId
               AND sp.userId = ?
               AND sp.id != ?
           )
         )
      ORDER BY ra.confidence DESC
    `),
    localPaperByEntity: db.prepare(`
      SELECT id, title, year, authors
      FROM Paper
      WHERE entityId = ? AND userId = ?
      LIMIT 1
    `),
  };
}

const PROVENANCE_PRIORITY = {
  reference_match: 0,
  citation_analysis: 1,
  discovery: 2,
  llm_semantic: 3,
  user_manual: 4,
};

function listCurrentRouteRows(prepared, paper) {
  if (paper.entityId) {
    const assertions = prepared.aggregateAssertions.all(
      paper.id,
      paper.entityId,
      paper.userId,
      paper.id
    );
    const groups = new Map();

    for (const assertion of assertions) {
      const isOutbound = assertion.sourceEntityId === paper.entityId;
      const peerEntityId = isOutbound ? assertion.targetEntityId : assertion.sourceEntityId;
      if (!peerEntityId) continue;

      const peerPaper = prepared.localPaperByEntity.get(peerEntityId, paper.userId);
      if (!peerPaper) continue;

      const key = `${peerEntityId}::${assertion.relationType}`;
      const priority = PROVENANCE_PRIORITY[assertion.provenance] ?? 0;
      const existing = groups.get(key);

      if (existing) {
        existing.maxConfidence = Math.max(existing.maxConfidence, Number(assertion.confidence ?? 0));
        existing.provenances.add(assertion.provenance);
        if (assertion.description && priority > existing.bestDescriptionPriority) {
          existing.description = assertion.description;
          existing.bestDescriptionPriority = priority;
        }
        continue;
      }

      groups.set(key, {
        id: `agg::${peerEntityId}::${assertion.relationType}`,
        relatedPaper: peerPaper,
        relationType: assertion.relationType,
        description: assertion.description ?? null,
        bestDescriptionPriority: priority,
        maxConfidence: Number(assertion.confidence ?? 0),
        provenances: new Set([assertion.provenance]),
      });
    }

    const aggregateRows = [...groups.values()]
      .map((group) =>
        normalizeRelationRow({
          id: group.id,
          relatedPaper: group.relatedPaper,
          relationType: group.relationType,
          description: group.description,
          confidence: group.maxConfidence,
          isAutoGenerated: !group.provenances.has("user_manual"),
        })
      )
      .sort((a, b) => b.confidence - a.confidence);

    if (aggregateRows.length > 0) {
      return { mode: "aggregate", rows: aggregateRows };
    }
  }

  const seen = new Set();
  const rows = [];

  for (const relation of prepared.sourceRelations.all(paper.id)) {
    if (seen.has(relation.relatedPaperId)) continue;
    seen.add(relation.relatedPaperId);
    rows.push(
      normalizeRelationRow({
        id: relation.id,
        relatedPaper: {
          id: relation.relatedPaperId,
          title: relation.relatedPaperTitle,
          year: relation.relatedPaperYear,
          authors: relation.relatedPaperAuthors,
        },
        relationType: relation.relationType,
        description: relation.description,
        confidence: relation.confidence,
        isAutoGenerated: relation.isAutoGenerated,
      })
    );
  }

  for (const relation of prepared.targetRelations.all(paper.id)) {
    if (seen.has(relation.relatedPaperId)) continue;
    seen.add(relation.relatedPaperId);
    rows.push(
      normalizeRelationRow({
        id: relation.id,
        relatedPaper: {
          id: relation.relatedPaperId,
          title: relation.relatedPaperTitle,
          year: relation.relatedPaperYear,
          authors: relation.relatedPaperAuthors,
        },
        relationType: relation.relationType,
        description: relation.description,
        confidence: relation.confidence,
        isAutoGenerated: relation.isAutoGenerated,
      })
    );
  }

  rows.sort((a, b) => b.confidence - a.confidence);
  return { mode: "legacy", rows };
}

function hasStableIdentifier(paper) {
  return Boolean(paper.doi || paper.arxivId || paper.semanticScholarId);
}

function buildWriteVisibilityMap(prepared, papers) {
  const result = new Map();
  for (const paper of papers) {
    const routeState = listCurrentRouteRows(prepared, paper);
    result.set(paper.id, {
      owner_userId: paper.userId,
      rendered_in_relations_ui: routeState.rows.length > 0,
      checked_at: new Date().toISOString(),
    });
  }
  return result;
}

function buildCoverageReport(prepared, papers, waiversPayload, dbPath) {
  const visibility = buildWriteVisibilityMap(prepared, papers);
  const waiverEntries = Array.isArray(waiversPayload?.waivers) ? waiversPayload.waivers : [];
  const { normalizedWaivers, errors } = validateWaivers(waiverEntries, visibility);
  if (errors.length > 0) {
    throw new Error(errors.join("\n"));
  }

  const waivedPaperIds = new Set(normalizedWaivers.map((waiver) => waiver.paperId));
  const uncoveredPapers = [];
  let linkedCount = 0;
  let hydratableCount = 0;
  let unhydratableCount = 0;

  for (const paper of papers) {
    if (paper.entityId) {
      linkedCount++;
      continue;
    }

    if (waivedPaperIds.has(paper.id)) {
      continue;
    }

    if (hasStableIdentifier(paper)) {
      hydratableCount++;
      continue;
    }

    unhydratableCount++;
    uncoveredPapers.push({
      paperId: paper.id,
      userId: paper.userId,
      title: paper.title,
      year: paper.year,
      doi: paper.doi ?? null,
      arxivId: paper.arxivId ?? null,
      semanticScholarId: paper.semanticScholarId ?? null,
      reason: "missing_stable_identifiers",
    });
  }

  return {
    generatedAt: new Date().toISOString(),
    dbPath,
    denominatorSize: papers.length,
    linkedCount,
    hydratableCount,
    unhydratableCount,
    waivedCount: normalizedWaivers.length,
    coveredCount: linkedCount + hydratableCount + normalizedWaivers.length,
    uncoveredCount: unhydratableCount,
    uncoveredPapers,
    waivers: normalizedWaivers,
  };
}

function buildParityReport(prepared, papers, dbPath, baselinePayload, baselinePath) {
  const currentStates = new Map();
  for (const paper of papers) {
    currentStates.set(paper.id, {
      paperId: paper.id,
      userId: paper.userId,
      ...listCurrentRouteRows(prepared, paper),
    });
  }

  if (!baselinePayload) {
    const paperStates = [...currentStates.values()];
    return {
      generatedAt: new Date().toISOString(),
      dbPath,
      baselinePath: null,
      papersChecked: paperStates.length,
      exactParityPapers: paperStates.length,
      expectedModeSwitchPapers: 0,
      trueMismatches: 0,
      mismatchDetails: [],
      multiplicity: {
        papersWithFanOut: 0,
        details: [],
      },
      papers: paperStates,
    };
  }

  const baselineStates = new Map(
    (baselinePayload.papers ?? []).map((paper) => [paper.paperId, paper])
  );

  const papersOut = [];
  const mismatchDetails = [];
  const fanOutDetails = [];
  let exactParityPapers = 0;
  let expectedModeSwitchPapers = 0;
  let trueMismatches = 0;

  for (const paper of papers) {
    const before = baselineStates.get(paper.id);
    const after = currentStates.get(paper.id);
    if (!before || !after) continue;

    const result = classifyParityChange({
      beforeMode: before.mode,
      beforeRows: before.rows,
      afterMode: after.mode,
      afterRows: after.rows,
    });

    const entry = {
      paperId: paper.id,
      userId: paper.userId,
      beforeMode: before.mode,
      afterMode: after.mode,
      beforeRows: before.rows,
      afterRows: after.rows,
      classification: result.classification,
      multiplicity: result.multiplicity,
      mismatchDetails: result.mismatchDetails,
    };
    papersOut.push(entry);

    if (result.classification === "exact_parity") exactParityPapers++;
    if (result.classification === "expected_mode_switch") expectedModeSwitchPapers++;
    if (result.classification === "true_mismatch") {
      trueMismatches++;
      mismatchDetails.push(entry);
    }
    if (result.multiplicity.fanOutPeers.length > 0) {
      fanOutDetails.push({
        paperId: paper.id,
        peerCountBefore: result.multiplicity.peerCountBefore,
        peerCountAfter: result.multiplicity.peerCountAfter,
        rowCountBefore: result.multiplicity.rowCountBefore,
        rowCountAfter: result.multiplicity.rowCountAfter,
        fanOutPeers: result.multiplicity.fanOutPeers,
      });
    }
  }

  return {
    generatedAt: new Date().toISOString(),
    dbPath,
    baselinePath,
    papersChecked: papersOut.length,
    exactParityPapers,
    expectedModeSwitchPapers,
    trueMismatches,
    mismatchDetails,
    multiplicity: {
      papersWithFanOut: fanOutDetails.length,
      details: fanOutDetails,
    },
    papers: papersOut,
  };
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const db = new Database(args.dbPath, { readonly: true, fileMustExist: true });

  try {
    const prepared = makePrepared(db);
    const papers = prepared.denominatorPapers.all();
    const waivers = readJson(args.waiversPath, emptyWaiversFile());
    const baseline = readJson(args.baselinePath, null);

    const coverage = buildCoverageReport(prepared, papers, waivers, args.dbPath);
    const parity = buildParityReport(prepared, papers, args.dbPath, baseline, args.baselinePath);

    if (args.entityOut) writeJson(args.entityOut, coverage);
    if (args.parityOut) writeJson(args.parityOut, parity);

    if (!args.entityOut && !args.parityOut) {
      console.log(JSON.stringify({ coverage, parity }, null, 2));
    }
  } finally {
    db.close();
  }
}

main();
